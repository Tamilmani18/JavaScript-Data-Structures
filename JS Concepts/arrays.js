// Array Methods

// map()
// iterate over the arr elements & do some operation & return the new copy of array

const myArray = [1, 2, 3, 4, 5];

console.log(myArray);

const newArray = myArray.map((el) => el + 1);

console.log(newArray);

const bArray = myArray.map((el) => "b");

console.log(bArray);

